public class Ifelseif
{
    public static void main(String args[])
    {
        int a =  50;
        if(a==30)
        {
            System.out.println("a is 30");
        }
        else if(a==45)
        {
            System.out.println("a is 45");
        }
        else if(a==50)
        {
            System.out.println("a is 50");
        }
        else
        {
            System.out.println("a is not present");
        }
    }
}