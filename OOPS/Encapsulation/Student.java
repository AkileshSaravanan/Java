public class Student
{
    private String Name;
    private int Rollno;
    private int Age;
    public String getName()
    {
        return Name;
    }
    public void setName(String Name)
    {
        this.Name = Name;
    }
    public int getRollno()
    {
        return Rollno;
    }
    public void setRollno(int Rollno)
    {
        this.Rollno = Rollno;
    }
    public int getAge()
    {
        return Age;
    }
    public void setAge(int Age)
    {
        this.Age = Age;
    }
}