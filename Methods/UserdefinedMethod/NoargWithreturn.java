public class NoargWithreturn
{
    public static int SumofTwoNumbers()
    {
        int a = 24;
        int b = 20;
        int sum = a + b;
        return sum;      
    }
    public static void main(String args[])
    {
        int sum = SumofTwoNumbers();
        System.out.println(sum);
    }
}
